package com.pmac.asses.pojo;

public class SoccerData {
	String team;
	Integer forGoal;
	Integer againstGoal;

	public String getTeam() {
		return team;
	}

	public void setTeam(String team) {
		this.team = team;
	}

	public Integer getForGoal() {
		return forGoal;
	}

	public void setForGoal(Integer forGoal) {
		this.forGoal = forGoal;
	}

	public Integer getAgainstGoal() {
		return againstGoal;
	}

	public void setAgainstGoal(Integer againstGoal) {
		this.againstGoal = againstGoal;
	}
}
